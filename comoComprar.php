<h4> Comprar na Surf Store OnLine é muito fácil, rápido e seguro.<br/>

Veja abaixo os passos para efetuar uma compra: </h4> 

<br/>
<h4><b>1. Navegação na loja.</b></h4>
Navegue pela loja pelos menus laterais e confira os produtos que a Surf Store  OnLine oferece pra você. Se preferir, digite o nome de um produto no campo BUSCA no topo do site.<br/><br/>




 

<h4><b>2. Escolha um produto.</h4></b>
Ao clicar sobre um produto você visualiza a descrição, vídeos, medidas, preço, condições de pagamento e fotos (clique sobre a imagem para ampliar). Você pode também solicitar maiores informações sobre o produto, pedir este produto de presente para alguém ou mesmo indicar este produto para um amigo. Clique em COMPRAR para adicionar o produto escolhido à sua lista de compras.<br/><br/>




 

<h4><b>3. Fechar pedido.</h4></b>
Na lista de compras você verá todos os produtos que selecionou. Digite o CEP do endereço para entrega e clique OK. Em seguida, selecione a forma de entrega (FRETE GRÁTIS PARA TODO BRASIL nas compras acima de R$ 199,90 na modalidade PAC). Clique em CONTINUAR para prosseguir.<br/><br/>




 

<h4><b>4. Login.</h4></b>
PARA COMPRAR É PRECISO ESTAR LOGADO.<br/>

Se você já é cliente, digite seu e-mail e senha. Caso ainda não tenha uma conta, clique para fazer o cadastro. Seus dados estarão protegidos e criptografados. Em hipótese alguma serão fornecidos para terceiros.<br/><br/>




 

<h4><b>5. Escolha a forma de pagamento.</h4></b>
Escolha a forma de pagamento de sua preferência. Se optar por cartão de crédito, fique tranquilo, seus dados não serão armazenados. Todas as opções são para pagamento online, exceto Boleto Bancário que deverá ser impresso e pago em qualquer agência, ou pelo seu Internet Banking. Somente após o pagamento e compensação do boleto, é que seu pedido será enviado; não há necessidade do envio de comprovante de pagamento. Os pedidos realizados por cartão de crédito estão sujeitos a análise e poderão ser solicitados dados que comprovem o titular do cartão.<br/><br/>




 

<h4><b>6. Método de pagamento escolhido. Pedido concluído!</h4></b>
Após confirmar a compra, será enviado para seu e-mail cadastrado o Comprovante do Pedido, este comprovante contém todos os dados da sua compra e serve como referências futuras. Assim que o Status do Pedido for alterado, você receberá um e-mail, de confirmação com o código de rastreamento (acompanhe seu pedido passo a passo até recebê-lo em mãos).<br/><br/>




 

<h4><b>7. Fim da compra.</h4></b>
Viu como foi fácil? Você poderá acessar a qualquer momento o serviço MEUS PEDIDOS no topo ou no rodapé da loja, onde terá acesso a todos os dados da sua compra e também poderá imprimir novamente o boleto se necessário.<br/><br/>




 

 

